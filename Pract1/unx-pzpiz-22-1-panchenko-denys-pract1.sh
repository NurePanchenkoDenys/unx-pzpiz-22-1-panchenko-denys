#!/bin/bash

# =============================================================================
# Практичне заняття №1 — Операційні системи Unix
# Тема: Розробка Bash-скриптів з використанням керуючих конструкцій
# Автор: Панченко Денис, гр. ПЗПІз-22-1
# Опис: Скрипт виводить текстове зображення «ялинки» із символів #
# Використано усі 5 обов'язкових керуючих конструкцій
# =============================================================================

# --- Кількість ярусів за замовчуванням ---
DEFAULT_TIERS=3

# --- Обробка аргументу командного рядка ---
# ============================================================
# Конструкція №1: if ... then ... fi
# Перевірка наявності та коректності аргументу
# ============================================================
if [ $# -ge 1 ]; then
    INPUT="$1"

    # Перевірка: чи є введене значення додатнім цілим числом
    if ! [[ "$INPUT" =~ ^[1-9][0-9]*$ ]]; then
        echo "Помилка: аргумент повинен бути додатнім цілим числом." >&2
        echo "Використання: $0 [кількість_ярусів]" >&2
        exit 1
    fi

    TIERS="$INPUT"
else
    TIERS="$DEFAULT_TIERS"
fi

# --- Параметри ялинки ---
TIER_ROWS=4             # Кількість рядків у кожному ярусі (3, 5, 7, 9)
MAX_HASHES=$((TIER_ROWS * 2 + 1))   # Максимальна ширина ярусу = 9
BASE_WIDTH=$((MAX_HASHES + 2))       # Ширина основи = 11
HALF=$((BASE_WIDTH / 2))             # Половина ширини для вирівнювання
TRUNK_WIDTH=3
TRUNK_HEIGHT=2

# --- Функція виведення одного рядка ---
# Приймає кількість символів # та виводить вирівняний рядок
print_row() {
    local hashes=$1
    local spaces=$(( HALF - hashes / 2 ))
    local line=""

    # ============================================================
    # Конструкція №5: for (( ... )); do ... done
    # Арифметичний цикл для формування рядка пробілів та символів
    # ============================================================
    for (( i = 0; i < spaces; i++ )); do
        line="${line} "
    done

    for (( i = 0; i < hashes; i++ )); do
        line="${line}#"
    done

    echo "$line"
}

# --- Виведення верхівки (один символ #) ---
print_row 1

# --- Виведення ярусів крони ---
CURRENT_TIER=1

# ============================================================
# Конструкція №2: while ... do ... done
# Цикл з передумовою — перебір ярусів ялинки
# ============================================================
while [ "$CURRENT_TIER" -le "$TIERS" ]; do
    ROW=0

    # ============================================================
    # Конструкція №3: until ... do ... done
    # Цикл з післяумовою — перебір рядків у межах ярусу
    # ============================================================
    until [ "$ROW" -ge "$TIER_ROWS" ]; do
        HASHES=$(( 3 + ROW * 2 ))   # 3, 5, 7, 9
        print_row "$HASHES"
        ROW=$(( ROW + 1 ))
    done

    CURRENT_TIER=$(( CURRENT_TIER + 1 ))
done

# --- Виведення стовбура ---
# ============================================================
# Конструкція №4: for ... in ... do ... done
# Цикл перебору списку — виведення рядків стовбура
# ============================================================
for LINE_NUM in $(seq 1 $TRUNK_HEIGHT); do
    print_row "$TRUNK_WIDTH"
done

# --- Виведення основи (підставки) ---
print_row "$BASE_WIDTH"

exit 0